function somaDoisNumeros(n1, n2) {
  return n1 + n2;
}
console.log(somaDoisNumeros(1, 2));
console.log(somaDoisNumeros(4, 3));
console.log(somaDoisNumeros(8, 7));

const somaTresNumeros = (n1, n2, n3) => n1 + n2 + n3; // <3
console.log(somaTresNumeros(4, 3, 1));
console.log(somaTresNumeros(8, 7, 3));

